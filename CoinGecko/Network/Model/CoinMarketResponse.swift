import Foundation


struct CoinMarket: Decodable {
    let id: String
    let symbol: String
    let name: String
    let image: String
    let currentPrice: Double //현재가(시가)
    let high24H: Double //코인 고가
    let low24H: Double //코인 저가
    let priceChangePercentage24H: Double //코인변동폭
    let ath: Double //코인 사상 최고가(신고점, All Time High)
    let athDate: String //신고점 일자
    let atl: Double //코인 사상 최저가(신저점, All Time Low)
    let atlDate: String //신저점 일자
    let lastUpdated: String //코인 시장 데이터 업데이트 시각
    let sparkLine7d: SparkLine //일주일간 코인 시세 정보
    
    var priceDescription: String {
        return currentPrice.toFormattedString
    }
    
    var highPriceDescription: String {
        return high24H.toFormattedString
    }
    
    var lowPriceDescription: String {
        return low24H.toFormattedString
    }
    
    var athDescription: String {
        return ath.toFormattedString
    }
    
    var atlDescription: String {
        return atl.toFormattedString
    }
    
    var percentDescription: String {
        let sign: String
        
        if priceChangePercentage24H > 0 {
            sign = "+"
        } else {
            sign = "-"
        }
        
        return sign + String(format: "%.2f", priceChangePercentage24H) + "%"
    }
    
    var updateDescription: String {
        return lastUpdated.dateFormatString() + " 업데이트"
    }
    
    enum CodingKeys: String, CodingKey {
        case id, symbol, name, image
        case currentPrice = "current_price"
        case high24H = "high_24h"
        case low24H = "low_24h"
        case priceChangePercentage24H = "price_change_percentage_24h"
        case ath
        case athDate = "ath_date"
        case atl
        case atlDate = "atl_date"
        case lastUpdated = "last_updated"
        case sparkLine7d = "sparkline_in_7d"
    }
    
}

